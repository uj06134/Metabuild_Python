# 문자열 결합 방법 3가지
print("오늘은" + "즐거운" + "금요일")   # + 연산자로 문자열 연결
print("오늘은" "즐거운" "금요일")       # 인접한 문자열은 자동으로 연결됨
print("오늘은", "즐거운", "금요일")     # 쉼표 사용 시 공백이 자동으로 삽입됨 → "오늘은 즐거운 금요일"

# 숫자와 문자열 연산 비교
print(12 + 34)            # 정수 덧셈 → 46
print("12+34")            # 문자열 그대로 출력 → "12+34"
print(12 + int("34"))     # 문자열 "34"를 정수로 변환 후 덧셈 → 46
print("12" + str(34))     # 숫자 34를 문자열로 변환 후 연결 → "1234"

# type()으로 자료형 확인
print(type(12))           # <class 'int'>
print(type("12"))         # <class 'str'>

# 서식 지정자(%)
print("%d / %d = %d" % (10, 3, 10/3))     # %d: 정수형, 실수 10/3을 정수로 변환 → 3
print("%f / %f = %f" % (10, 3, 10/3))     # %f: 실수형 기본 6자리 소수 출력
print("%f / %f = %.2f" % (10, 3, 10/3))   # %.2f: 소수 둘째 자리까지 출력 → 3.33
print("%d / %d = %5d" % (10, 3, 10/3))    # %5d: 5칸 폭으로 정수 출력 (빈칸은 공백)
print("%s" % "python")                    # %s: 문자열 출력

# 문자열 * 숫자 → 반복 출력
print("A" * 3)             # "AAA"

# 산술 연산
print(15 / 6)              # 나누기(실수 결과) → 2.5
print(15 // 6)             # 몫 연산 → 2
